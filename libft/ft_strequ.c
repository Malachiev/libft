#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	if (s1 && s2)
	{
		i = ft_strcmp(s1, s2);
		if (!i)
		{
			return (1);
		}
	}
	return (0);
}
