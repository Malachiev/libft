#include "libft.h"

void	ft_striter(char *str, void (*f)(char*))
{
	size_t	i;

	i = 0;
	if (!str || !f)
	{
		return ;
	}
	while (str[i] != '\0')
	{
		f((char*)&str[i]);
		i++;
	}
}
